$(document).ready(function(){
     $('#edit').click(function(){
        $('#edit-modal').modal('show');    
     });
});
$(document).ready(function(){
     $('#cancel').click(function(){
        $('#edit-modal').modal('hide');    
     });
});
$(document).ready(function(){
     $('#delete').click(function(){
        $('#delete-modal').modal('show');    
     });
});